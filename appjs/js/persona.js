class Persona {
    constructor(nombre, apellido, edad, email, usuario, contraseña, mayorDeEdad) {
        this.nombre = nombre;
        this.email = email;
        this.edad = edad;
        this.apellido = apellido;
        this.usuario = usuario;
        this.contraseña = contraseña;
        this.mayorDeEdad = mayorDeEdad;
    }
}
